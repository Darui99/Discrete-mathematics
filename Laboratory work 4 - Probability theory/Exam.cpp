#include <bits/stdc++.h>
using namespace std;

typedef long long ll;
typedef long double ld;

int main() {
    freopen("exam.in", "r", stdin);
    freopen("exam.out", "w", stdout);
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
	
	ll n, sum = 0ll, m, a, b;
	cin >> n >> m;

	while (n--) {
		cin >> a >> b;
		sum += a * b;
	}

	ld ans = (ld)sum / (ld)(m * 100);

	cout.precision(20);
	cout << fixed << ans;

	return 0;
}
