#include <bits/stdc++.h>
using namespace std;

typedef long long ll;
typedef long double ld;

int main() {
    freopen("lottery.in", "r", stdin);
    freopen("lottery.out", "w", stdout);
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
	
	ld s, ans = (ld)0.0, p = (ld)1.0;
	cin >> s;

	int n;
	cin >> n;

	vector < pair<ld, ld> > arr(n);
	for (int i = 0; i < n; i++) {
		cin >> arr[i].first >> arr[i].second;
	}

	for (int i = 0; i < n; i++) {
		p /= arr[i].first;
		if (i < n - 1) {
			ans += p * (arr[i + 1].first - (ld)1.0) / arr[i + 1].first * arr[i].second;
		} else {
			ans += p * arr[i].second;
		}
	}

	cout.precision(20);
	cout << fixed << s - ans;

	return 0;
}
