#include <bits/stdc++.h>
using namespace std;

int n;
vector < vector<int> > ans;
vector <int> arr;

void rec(int pos) {
	if (pos == n) {
		ans.push_back(arr);
		return;
	}

	rec(pos + 1);
	arr.push_back(pos + 1);
	rec(pos + 1);
	arr.pop_back();
}

int main() {
    freopen("subsets.in", "r", stdin);
    freopen("subsets.out", "w", stdout);
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	cin >> n;
	rec(0);

	sort(ans.begin(), ans.end());
	for (int j = 0; j < (int)ans.size(); j++) {
		for (int i = 0; i < (int)ans[j].size(); i++)
			cout << ans[j][i] << " ";
		cout << '\n';
	}

	return 0;
}
