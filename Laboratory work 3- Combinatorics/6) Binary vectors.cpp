#include <bits/stdc++.h>
using namespace std;

int n;
string s = "";
vector <string> ans;

void rec(int pos) {
	if (pos == n) {
		bool flag = false;
		for (int i = 1; i < n; i++)
			flag |= (s[i] == '1' && s[i - 1] == '1');
		if (!flag)
			ans.push_back(s);
		return;
	}
	s.push_back('0');
	rec(pos + 1);
	s.pop_back();
	s.push_back('1');
	rec(pos + 1);
	s.pop_back();
}

int main() {
    freopen("vectors.in", "r", stdin);
    freopen("vectors.out", "w", stdout);
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	cin >> n;
	rec(0);

	cout << (int)ans.size() << '\n';
	for (int i = 0; i < (int)ans.size(); i++)
		cout << ans[i] << '\n';

	return 0;
}
