#include <bits/stdc++.h>
using namespace std;

int n, k;
vector <int> arr;

void rec(int pos, int prev) {
	if (pos == k) {
		for (int i = 0; i < k; i++)
			cout << arr[i] + 1 << ' ';
		cout << '\n';
		return;
	}

	if (k - pos > n - prev - 1)
		return;
	for (int i = prev + 1; i < n; i++) {
		arr.push_back(i);
		rec(pos + 1, i);
		arr.pop_back();
	}
}


int main() {
    freopen("choose.in", "r", stdin);
    freopen("choose.out", "w", stdout);
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	cin >> n >> k;
	rec(0, -1);

	return 0;
}
