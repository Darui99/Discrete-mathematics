#include <bits/stdc++.h>
using namespace std;

int n;
string s = "";

void rec(int pos) {
	if (pos == n) {
		cout << s << '\n';
		return;
	}
	s.push_back('0');
	rec(pos + 1);
	s.pop_back();
	s.push_back('1');
	rec(pos + 1);
	s.pop_back();
}

int main() {
  freopen("allvectors.in", "r", stdin);
  freopen("allvectors.out", "w", stdout);
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	cin >> n;
	rec(0);

	return 0;
}
