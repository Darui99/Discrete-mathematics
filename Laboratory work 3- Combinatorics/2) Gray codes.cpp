#include <bits/stdc++.h>
using namespace std;

int n;

string convert(int x) {
	string res = "";
	while (x) {
		res.push_back((char)(x % 2 + '0'));
		x /= 2;
	}
	while ((int)res.size() < n)
		res.push_back('0');
	return res;
}

int main() {
  freopen("gray.in", "r", stdin);
  freopen("gray.out", "w", stdout);
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	cin >> n;
	for (int i = 0; i < (1 << n); i++)
		cout << convert(i ^ (i >> 1)) << '\n';

	return 0;
}
