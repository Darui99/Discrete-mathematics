#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
 
int main() {
    freopen("nextchoose.in", "r", stdin);
    freopen("nextchoose.out", "w", stdout);
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);
 
    int n, m;
	cin >> n >> m;

	vector <int> arr(m);
	for (int i = 0; i < m; i++)
		cin >> arr[i];

	for (int i = m - 1; i >= 0; i--) {
		int can = n - arr[i] - 1;
		if (can >= m - i - 1) {
			arr[i]++;
			for (int j = i + 1; j < m; j++)
				arr[j] = arr[j - 1] + 1;
			for (int j = 0; j < m; j++)
				cout << arr[j] << " ";
			return 0;
		}
	}
	cout << -1;
         
    return 0;
}
