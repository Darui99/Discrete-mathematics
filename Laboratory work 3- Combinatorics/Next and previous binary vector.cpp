#include <bits/stdc++.h>
using namespace std;
typedef long long ll;

int main() {
    freopen("nextvector.in", "r", stdin);
    freopen("nextvector.out", "w", stdout);
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	string s;
	cin >> s;
	int n = (int)s.size();

	string next = s;
	for (int i = n - 1; i >= 0; i--) {
		if (next[i] == '0') {
			next[i] = '1';
			break;
		} else {
			next[i] = '0';
		}
	}

	string prev = s;
	for (int i = n - 1; i >= 0; i--) {
		if (prev[i] == '1') {
			prev[i] = '0';
			break;
		} else {
			prev[i] = '1';
		}
	}

	if (prev > s)
		cout << "-\n";
	else
		cout << prev << '\n';

	if (next < s)
		cout << "-\n";
	else
		cout << next << '\n';
		
	return 0;
}
