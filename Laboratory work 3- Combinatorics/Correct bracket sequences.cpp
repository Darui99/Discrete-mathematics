#include <bits/stdc++.h>
using namespace std;

int n;
string s;

void rec(int pos, int d) {
	if (pos == 2 * n) {
		cout << s << '\n';
		return;
	}
	
	if (2 * n - pos - 1 >= d + 1) {
		s.push_back('(');
		rec(pos + 1, d + 1);
		s.pop_back();
	}
	if (d > 0) {
		s.push_back(')');
		rec(pos + 1, d - 1);
		s.pop_back();
	}
}

int main() {
    freopen("brackets.in", "r", stdin);
    freopen("brackets.out", "w", stdout);
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	cin >> n;
	rec(0, 0);

	return 0;
}
