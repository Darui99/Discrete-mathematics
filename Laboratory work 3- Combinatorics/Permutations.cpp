#include <bits/stdc++.h>
using namespace std;

int n;
vector <bool> used;
vector <int> arr;

void rec(int pos) {
	if (pos == n) {
		for (int i = 0; i < n; i++)
			cout << arr[i] + 1 << ' ';
		cout << '\n';
		return;
	}

	for (int i = 0; i < n; i++) {
		if (!used[i]) {
			used[i] = true;
			arr.push_back(i);
			rec(pos + 1);
			arr.pop_back();
			used[i] = false;
		}
	}
}

int main() {
    freopen("permutations.in", "r", stdin);
    freopen("permutations.out", "w", stdout);
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	cin >> n;
	used.resize(n);
	rec(0);

	return 0;
}
