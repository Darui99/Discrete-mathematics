#include <bits/stdc++.h>
using namespace std;

vector <int> arr;

void rec(int n, int prev) {
	if (n == 0) {
		for (int i = 0; i < (int)arr.size() - 1; i++)
			cout << arr[i] << "+";
		cout << arr.back() << '\n';
		return;
	}
	for (int i = prev; i <= n; i++) {
		if (n - i >= prev || n - i == 0) {
			arr.push_back(i);
			rec(n - i, i);
			arr.pop_back();
		}
	}
}

int main() {
    freopen("partition.in", "r", stdin);
    freopen("partition.out", "w", stdout);
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	int n;
	cin >> n;

	rec(n, 1);

	return 0;
}
